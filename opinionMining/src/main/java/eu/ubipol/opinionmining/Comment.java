package eu.ubipol.opinionmining;

import java.util.Date;

import eu.ubipol.opinionmining.Domain;







/**************************************************************************
 *
 * A comment for processing by the opinion mining engine.
 *
 **************************************************************************/

public interface Comment {





/**************************************************************************
 *
 * @return The comment unique ID.
 *
 **************************************************************************/

    Long getId();





/**************************************************************************
 *
 * @return The free form text associated with this comment.
 *
 **************************************************************************/

    String getText();





/**************************************************************************
 *
 * @return The date this comment was created.
 *
 **************************************************************************/

    Date getDate();





/**************************************************************************
 *
 * @return The domain this comment is associated with.
 *
 **************************************************************************/

    Domain getDomain();


}





