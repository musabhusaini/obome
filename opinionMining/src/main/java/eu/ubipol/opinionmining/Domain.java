package eu.ubipol.opinionmining;





/**************************************************************************
 *
 * A domain in an opinion mining engine.
 *
 **************************************************************************/

public interface Domain {





/**************************************************************************
 *
 * Retrieves this domain unique ID. All the domains in one opinion
 * mining engine must have unique IDs.
 *
 * @return The domain unique ID.
 *
 **************************************************************************/

    Long getId();





/**************************************************************************
 *
 * @return The human readable title of this domain.
 *
 **************************************************************************/

    String getText();


}





